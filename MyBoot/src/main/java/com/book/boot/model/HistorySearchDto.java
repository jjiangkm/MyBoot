package com.book.boot.model;

public class HistorySearchDto {
	private String v;
	private String key;
	private int month;
	private int day;
	public String getV() {
		return v;
	}
	public void setV(String v) {
		this.v = v;
	}
	public String getKey() {
		return key;
	}
	public void setKey(String key) {
		this.key = key;
	}
	public int getMonth() {
		return month;
	}
	public void setMonth(int month) {
		this.month = month;
	}
	public int getDay() {
		return day;
	}
	public void setDay(int day) {
		this.day = day;
	}
	
}
